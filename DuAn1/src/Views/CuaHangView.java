    public void showDaTa(List<CuaHangViewModel> lst) {
        dtm.setRowCount(0);
        for (CuaHangViewModel ch : lst) {
            dtm.addRow(ch.toRowDaTa());
        }
    }

    public void showDaTaS(List<CuaHang> lsts) {
        dtm.setRowCount(0);
        for (CuaHang ch : lsts) {
            dtm.addRow(ch.toRowDaTa());
        }
    }

