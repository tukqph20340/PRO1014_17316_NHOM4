    private void bt_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SuaActionPerformed
        String maCH = txt_MaCh.getText();
        CuaHang x = new CuaHang();
        x.setTenCh(txt_TenCH.getText());
        x.setDiaChi(txt_DiaChi.getText());
        x.setThanhPho(txt_ThanhPho.getText());

        try {
            if (txt_MaCh.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã Cửa Hàng không được để trống");
                return;
            }
            /*for (String c : service.getListMaCH()) {
                if (txtMaCH.getText().equals(c) == false) {
                    JOptionPane.showMessageDialog(this, "Mã Cửa Hàng không tồn tại");
                    return;
                }
            }*/
            if (checkMa() == false) {
                JOptionPane.showMessageDialog(this, "Mã Cửa Hàng không tồn tại");
                return;
            }
            if (txt_TenCH.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tên Cửa Hàng không được để trống");
                return;
            }
            if (txt_DiaChi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Địa Chỉ không được để trống");
                return;
            }
            if (txt_ThanhPho.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Thành Phố không được để trống");
                return;
            }
            if (ser.sua(x, maCH)) {

                list = ser.getAll();
                showDaTa(list);
                JOptionPane.showMessageDialog(this, "Sửa Thành Công");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_bt_SuaActionPerformed
    public boolean checkMa() {
        String ma = txt_MaCh.getText();
        for (String c : ser.getListMaCH()) {
            if (txt_MaCh.getText().equals(c)) {
                return true;
            }
        }
        return false;
    }
    private void bt_ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_ADDActionPerformed
        CuaHang x = new CuaHang();
        x.setMaCh(txt_MaCh.getText());
        x.setTenCh(txt_TenCH.getText());
        x.setDiaChi(txt_DiaChi.getText());
        x.setThanhPho(txt_ThanhPho.getText());
        try {
            if (txt_MaCh.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã Cửa Hàng không được để trống");
                return;
            }
            /*for (String c : service.getListMaCH()) {
                if (txtMaCH.getText().equals(c) == true) {
                    JOptionPane.showMessageDialog(this, "Mã Cửa Hàng đã được sử dụng");
                    return;
                }
            }*/
            if (checkMa()) {
                JOptionPane.showMessageDialog(this, "Mã Cửa Hàng đã được sử dụng");
                return;
            }
            if (txt_TenCH.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tên Cửa Hàng không được để trống");
                return;
            }
            if (txt_DiaChi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Địa Chỉ không được để trống");
                return;
            }
            if (txt_ThanhPho.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Thành Phố không được để trống");
                return;
            }

            if (ser.insert(x)) {
                list = ser.getAll();
                JOptionPane.showMessageDialog(this, "Thêm Thành Công");
                showDaTa(list);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_bt_ADDActionPerformed

