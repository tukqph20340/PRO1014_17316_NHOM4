        // TODO add your handling code here:
        String madtct = txt_madtct.getText();
        String madt = txt_madt.getText();
        String mota = txt_mota.getText();
        String soluong = txt_soluong.getText();
        String gianhap = txt_gianhap.getText();
        String giaban = txt_giaban.getText();

        DienThoaiChiTiet dienThoaiChiTiet = new DienThoaiChiTiet();
        dienThoaiChiTiet.setMactsp(madtct);
        dienThoaiChiTiet.setMadt(madt);
        dienThoaiChiTiet.setMota(mota);

        dienThoaiChiTiet.setSoluong(soluong);
        dienThoaiChiTiet.setGianhap(gianhap);
        dienThoaiChiTiet.setGiaban(giaban);
        JOptionPane.showMessageDialog(this, cTSPService.themCTSP(dienThoaiChiTiet));
        addTable(cTSPService.getDsCTSP());
