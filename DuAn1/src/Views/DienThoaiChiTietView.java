    public void addTable(ArrayList<CTSPViewModel> list) {
        defaultTableModel = (DefaultTableModel) tbl_ctsp.getModel();
        defaultTableModel.setRowCount(0);
        for (CTSPViewModel cTSPViewModel : list) {
            defaultTableModel.addRow(new Object[]{
                cTSPViewModel.getMactsp(),
                cTSPViewModel.getMadt(),
                cTSPViewModel.getMota(),
                cTSPViewModel.getSoluong(),
                cTSPViewModel.getGianhap(),
                cTSPViewModel.getGiaban()});

        }

    }

    

