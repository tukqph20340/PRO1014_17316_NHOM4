    public void check() {
        if (txtMaCC.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp không được trống!");
            return;
        }
        if (txtTencc.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên cung cấp không được trống!");
            return;
        }
        if (txtDiaChi.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, " Địa chỉ không được trống!");
            return;
        }
        if (txtsdt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được trống!");
            return;
        }
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtMaCC.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp không được trống!");
            return;
        }
        if (txtTencc.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên cung cấp không được trống!");
            return;
        }
        if (txtDiaChi.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, " Địa chỉ không được trống!");
            return;
        }
        if (txtsdt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được trống!");
            return;
        }
        JOptionPane.showMessageDialog(this, ser.AddFormNhaCC(filldata()));
        list = ser.fina();
        fill(list);
        clear();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int a = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn sửa không?", "Sửa", JOptionPane.YES_NO_CANCEL_OPTION);
        if (a == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, ser.UpdateFormNhaCC(filldata()));
            list = ser.fina();
            fill(list);
            clear();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int a = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn xóa không?", "Xóa", JOptionPane.YES_NO_CANCEL_OPTION);
        if (a == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, ser.DeleteFormNhaCC(filldata()));
            list = ser.fina();
            fill(list);
            clear();
        }
    }//GEN-LAST:event_btnXoaActionPerformed
