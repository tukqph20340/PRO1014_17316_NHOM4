    public void fill(List<Nhacungcap_model> list) {
        model.setRowCount(0);
        model = (DefaultTableModel) tblNhacc.getModel();
        for (Nhacungcap_model nhacungcap_model : list) {
            model.addRow(nhacungcap_model.data());
        }
    }
    public void mouclick(List<Nhacungcap_model> list,int dex){
        txtMaCC.setText(list.get(dex).getMaCC());
        txtTencc.setText(list.get(dex).getTenCC());
        txtDiaChi.setText(list.get(dex).getDiaChi());
        txtsdt.setText(list.get(dex).getSdt());
    }
    public Nhacungcap_model filldata() {
        return new Nhacungcap_model(txtMaCC.getText(), txtTencc.getText(), txtDiaChi.getText(), txtsdt.getText());
    }
